void InstallerState::toggleActivity(const QString &name) {
    if (m_activeActivities.contains(name)) {
        m_activeActivities.removeAll(name);
    } else {
        m_activeActivities.append(name);
        m_activeActivities.sort();
    }
    emit activeActivitiesChanged();
}

bool InstallerState::hasActivity(const QString &name) const {
    return m_activeActivities.contains(name);
}

// Maps PRD Sec 3.1 desktop choice to the activity-shape `dkPlasma` /
// `dkGnome` / `dkSway` / `dkHyprland` variant kinds the
// ReproOS-Configuration-Architecture document defines.
static QString desktopKindToVariant(const QString &kind) {
    if (kind == "plasma") return "dkPlasma";
    if (kind == "gnome") return "dkGnome";
    if (kind == "sway") return "dkSway";
    if (kind == "hyprland") return "dkHyprland";
    return "dkPlasma";
}

QString InstallerState::renderSystemNim() const {
    // Render the user-facing system.nim per PRD Sec 3.3. The format is
    // pinned by ReproOS-Configuration-Architecture; the
    // `import repro_profile` + `system "<name>":` header is canonical.
    QString out;
    QTextStream s(&out);
    s << "# /etc/repro/system.nim --- generated by the ReproOS installer\n";
    s << "import repro_profile\n\n";
    s << "system \"" << m_hostname << "\":\n";
    s << "  imports:\n";
    s << "    \"./hardware.nim\"\n";
    for (const QString &act : m_activeActivities) {
        s << "    \"modules/activities/" << act << ".nim\"\n";
    }
    s << "    \"modules/de/" << m_desktopKind << ".nim\"\n";
    s << "    \"modules/networking/networkmanager.nim\"\n\n";
    s << "  config:\n";
    s << "    hostname: string = \"" << m_hostname << "\"\n";
    s << "    timezone: string = \"" << m_timezone << "\"\n";
    s << "    locale: string = \"" << m_locale << "\"\n";
    s << "    keymap: string = \"" << m_keymap << "\"\n";
    s << "    activeAtBoot: DesktopKind = " << desktopKindToVariant(m_desktopKind) << "\n";
    s << "    defaultUser: string = \"" << m_username << "\"\n\n";
    s << "  users:\n";
    s << "    \"" << m_username << "\":\n";
    s << "      fullName: \"" << m_fullName << "\"\n";
    s << "      groups: @[";
    s << "\"audio\", \"video\", \"networkmanager\"";
    if (m_isAdmin) s << ", \"wheel\"";
    s << "]\n";
    s << "      homeIntent: import \"./home.nim\"\n";
    return out;
}

// M9.R.23.2 -- render the M9.R.22 disko block matching the chosen
// preset. The "advanced" preset produces a hardware "<id>": block with
// no disko: sub-block + a comment instructing the user to add one
// post-install.
QString InstallerState::renderDiskoNim(const QString &id) const {
    QString out;
    QTextStream s(&out);
    s << "# /etc/repro/hardware.nim --- generated by the ReproOS installer\n";
    s << "import repro_profile\n\n";
    s << "hardware \"" << id << "\":\n";
    s << "  cpu:\n";
    s << "    arch: \"x86_64\"\n";
    s << "    microcode: \"intel\"\n";
    s << "  boot:\n";
    s << "    loaderDevice: \"" << (m_targetDevice.isEmpty()
        ? QStringLiteral("/dev/sda") : m_targetDevice) << "\"\n";

    if (m_diskoPreset == "advanced") {
        s << "  # disko: block omitted -- the user opted for the\n";
        s << "  # advanced layout. Hand-author the disks/pools blocks\n";
        s << "  # via the M9.R.22 macro vocabulary; see\n";
        s << "  # reprobuild-specs/ReproOS-Disko-Port.md for examples.\n";
        return out;
    }

    const QString dev = m_targetDevice.isEmpty()
        ? QStringLiteral("/dev/sda") : m_targetDevice;

    s << "  disko:\n";
    s << "    disks:\n";
    s << "      \"main\":\n";
    s << "        device: \"" << dev << "\"\n";
    s << "        table: gpt\n";
    s << "        partitions:\n";
    s << "          \"esp\":\n";
    s << "            kind: esp\n";
    s << "            size: \"512M\"\n";
    s << "            bootable: true\n";
    s << "            content:\n";
    s << "              filesystem:\n";
    s << "                format: \"vfat\"\n";
    s << "                mountpoint: \"/boot\"\n";
    s << "          \"root\":\n";
    s << "            kind: linux\n";
    s << "            size: \"100%\"\n";
    s << "            content:\n";

    if (m_diskoPreset == "encrypted") {
        // LUKS2 + btrfs subvols. The keyFile = "interactive" signals
        // the M9.R.22b apply driver to consume the passphrase from the
        // CT_DISK_PASSPHRASE env var instead of a key file.
        s << "              encrypted:\n";
        s << "                encryption:\n";
        s << "                  kind: \"luks2\"\n";
        s << "                  keyFile: \"interactive\"\n";
        s << "                  allowDiscards: true\n";
        s << "                inner:\n";
        s << "                  filesystem:\n";
        s << "                    format: \"btrfs\"\n";
        s << "                    mountpoint: \"/\"\n";
        s << "                    subvols:\n";
        s << "                      \"@\":\n";
        s << "                        path: \"/\"\n";
        s << "                      \"@home\":\n";
        s << "                        path: \"/home\"\n";
        s << "                      \"@nix\":\n";
        s << "                        path: \"/nix\"\n";
    } else {
        // simple preset (default): ext4 root.
        s << "              filesystem:\n";
        s << "                format: \"ext4\"\n";
        s << "                mountpoint: \"/\"\n";
        s << "                mountOptions: @[\"noatime\"]\n";
    }
    return out;
}

// M9.R.24.2 -- renderDiskoJson: pre-compute the SystemHardwareSpec
// JSON the disk_apply driver consumes, so the live ISO's installer
// can `repro disk apply --confirm <disko.json>` WITHOUT a Nim
// toolchain (the .nim path goes through `nim r` -- requires nim +
// gcc + libc-dev in the live filesystem, which we deliberately don't
// ship). The output must match libs/repro_profile/.../emit.nim's
// emitSystemHardwareJson for the same SystemHardwareSpec input.
//
// Currently supports the `simple` preset only; encrypted/advanced
// fall through to the .nim path (which still requires Nim).
QString InstallerState::renderDiskoJson(const QString &id) const {
    QString out;
    QTextStream s(&out);
    auto esc = [](const QString &x) {
        QString r;
        for (QChar c : x) {
            if (c == '"' || c == '\\') r += '\\';
            r += c;
        }
        return r;
    };
    const QString dev = m_targetDevice.isEmpty()
        ? QStringLiteral("/dev/vda") : m_targetDevice;
    // Filesystem content node (filesystem kind) -- includes all keys
    // parseContentSpec requires when kind==filesystem.
    auto fsContent = [&](const QString &format, const QString &mountpoint,
                         const QString &mountOpts) {
        QString r;
        r += "{\"kind\":\"filesystem\"";
        r += ",\"format\":\"" + esc(format) + "\"";
        r += ",\"mountpoint\":\"" + esc(mountpoint) + "\"";
        r += ",\"mountOptions\":" + mountOpts;
        r += ",\"label\":\"\"";
        r += ",\"subvols\":[]";
        r += "}";
        return r;
    };
    s << "{";
    s << "\"id\":\"" << esc(id) << "\"";
    s << ",\"cpuArch\":\"x86_64\"";
    s << ",\"cpuMicrocode\":\"intel\"";
    s << ",\"kernelModules\":[]";
    s << ",\"loaderDevice\":\"" << esc(dev) << "\"";
    s << ",\"filesystems\":[]";
    s << ",\"graphicsDrivers\":[]";
    s << ",\"audioCards\":[]";
    if (m_diskoPreset == "simple" || m_diskoPreset.isEmpty()) {
        s << ",\"disko\":{";
        s << "\"disks\":{";
        s << "\"main\":{";
        s << "\"device\":\"" << esc(dev) << "\"";
        s << ",\"type\":\"gpt\"";
        s << ",\"partitions\":{";
        // ESP partition.
        s << "\"esp\":{";
        s << "\"type\":\"esp\"";
        s << ",\"size\":\"512M\"";
        s << ",\"bootable\":true";
        s << ",\"content\":" << fsContent("vfat", "/boot", "[]");
        s << "},";
        // Root partition.
        s << "\"root\":{";
        s << "\"type\":\"linux\"";
        s << ",\"size\":\"100%\"";
        s << ",\"bootable\":false";
        s << ",\"content\":" << fsContent("ext4", "/", "[\"noatime\"]");
        s << "}";
        s << "}";
        s << "}";
        s << "}";
        s << ",\"pools\":[]";
        s << "}";
    }
    s << "}";
    return out;
}

