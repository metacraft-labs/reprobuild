// M9.R.23.2 -- lsblk-driven disk refresh.
// ---------------------------------------------------------------------

void InstallerState::refreshAvailableDisks() {
    QStringList disks;
#ifdef Q_OS_LINUX
    QProcess p;
    // -d: skip partitions/holders, -n: no header, -o: chosen columns,
    // -p: print full /dev/* path (we then strip the prefix for the
    // model so the QML side has both forms).
    p.start("lsblk", {"-d", "-n", "-o", "NAME,SIZE,MODEL,VENDOR"});
    if (!p.waitForStarted(2000)) {
        // lsblk missing -- fall back to a single placeholder so the QML
        // doesn't render an empty combo + the user can still see the
        // wizard works end-to-end on test hosts without util-linux.
        disks << "vda 16G QEMU";
        setAvailableDisks(disks);
        return;
    }
    p.waitForFinished(5000);
    const QString out = QString::fromUtf8(p.readAllStandardOutput());
    for (const QString &lineRaw : out.split('\n')) {
        const QString line = lineRaw.trimmed();
        if (line.isEmpty()) continue;
        // Filter loopback + ram devices; users don't want to install
        // onto loop0 or zram by accident.
        if (line.startsWith("loop") || line.startsWith("ram") ||
            line.startsWith("zram") || line.startsWith("sr"))
            continue;
        disks << line;
    }
#else
    // Non-Linux host (the Windows build of the installer is dev-only;
    // PRD Sec 7.1 pins the runtime to the live ISO + Wayland). Emit a
    // placeholder so the QML still works for screenshots.
    disks << "sda 100G Virtual";
    disks << "sdb 500G Virtual";
#endif
    setAvailableDisks(disks);
}

