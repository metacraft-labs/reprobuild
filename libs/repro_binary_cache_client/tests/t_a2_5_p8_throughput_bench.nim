const ServerBinary = "build/test-bin" / addFileExt("repro_binary_cache", ExeExt)
# 85 MiB matches the spec's R5 gcc-15.2.0 payload size. Bench runtimes
# are dominated by disk + memcpy; the synthetic payload's compressibility
# is irrelevant because A2.5 ships ckNone for v1 (zstd decompression is
# a separate measurement).
const BenchPayloadBytes = 85 * 1024 * 1024

proc pickPort(): int =
  randomize(); 23_000 + rand(8_999)

proc waitForListener(port: int; tries = 100; sleepMs = 50): bool =
  for _ in 0 ..< tries:
    try:
      let sock = newSocket()
      sock.connect("127.0.0.1", Port(port))
      sock.close()
      return true
    except CatchableError:
      sleep(sleepMs)
  return false

proc startServer(serverRoot: string; port: int): Process =
  startProcess(absolutePath(ServerBinary),
               args = @["--root=" & serverRoot,
                        "--listen=127.0.0.1:" & $port],
               options = {poStdErrToStdOut, poParentStreams})

proc generatePayload(sizeBytes: int; seed: int): seq[byte] =
  result = newSeq[byte](sizeBytes)
  var state = uint64(seed) or 0x1u64
  var i = 0
  while i < sizeBytes:
    state = state * 6364136223846793005'u64 + 1442695040888963407'u64
    let n = min(8, sizeBytes - i)
    for j in 0 ..< n:
      result[i + j] = byte((state shr (8 * j.uint64)) and 0xff)
    i += n

proc localPlatform(): PlatformTriple =
  # Mirror the client's own ``detectLocalPlatform`` so the synthesized
  # manifests key on the SAME platform triple the compat-check derives
  # for this host. Hardcoding ``x86_64`` made every substitute trip the
  # compat gate (``CPU mismatch: manifest=x86_64 local=aarch64``) on
  # arm64 macOS / Linux.
  let local = detectLocalPlatform("")
  PlatformTriple(cpu: local.cpu, os: local.os, abi: local.abi,
                 libcVariant: local.libcVariant)

suite "A2.5 P8 — throughput benchmark":
  test "85 MiB payload cold + warm substitution stay within budget":
    randomize()
    let port = pickPort()
    let serverRoot = getTempDir() / ("a2_5_p8_srv_" & $rand(999_999))
    let clientRoot = getTempDir() / ("a2_5_p8_cli_" & $rand(999_999))
    removeDir(serverRoot); removeDir(clientRoot)
    createDir(serverRoot); createDir(clientRoot)

    var state = openBinaryCacheServer(serverRoot)
    let payloadBytes = generatePayload(BenchPayloadBytes, seed = 0xb1d5)
    let payloadDigestRaw = blake3.digest(payloadBytes)
    var payloadDigest: Blake3Hash
    for i in 0 ..< 32: payloadDigest[i] = payloadDigestRaw[i]
    var realizedPrefix: Blake3Hash
    for i in 0 ..< 32: realizedPrefix[i] = byte((i * 19 + 5) and 0xff)
    let payload = PayloadObject(
      kind: pkPrefixArchive, compression: ckNone,
      declaredSize: uint64(payloadBytes.len),
      uncompressedSize: uint64(payloadBytes.len),
      digest: payloadDigest, name: "gcc-fixture.bin")
    let ek = CacheEntryKey(
      packageName: "gcc", packageVersion: "15.2.0",
      platform: localPlatform(),
      toolchain: ToolchainIdentity(name: "gcc", version: "11"),
      depClosureDigest: payloadDigest,
      providerRevision: "synthetic-85mib")
    var manifest = BinaryCacheManifest(
      formatVersion: BinaryCacheFormatVersion,
      entryKey: ek, payloads: @[payload],
      realizedPrefixDigest: realizedPrefix, depReferences: @[],
      relocationPolicy: rpOptional, createdAtUnix: 1)
    signManifest(state.producerKeypair, manifest)
    discard storeManifest(state, manifest)
    discard storePayload(state, payloadBytes)
    let entryKeyHex = cacheEntryKeyHex(manifest.entryKey)
    let pubKey = state.producerKeypair.publicKey
    close(state)

    let srvProc = startServer(serverRoot, port)
    defer:
      try: srvProc.terminate() except CatchableError: discard
      try: srvProc.close() except CatchableError: discard
    check waitForListener(port)

    let endpoint = SubstituteEndpoint(
      baseUrl: "http://127.0.0.1:" & $port,
      trustedSigners: @[pubKey])
    let cfg = defaultConfig(clientRoot, @[endpoint])
    let ctx = newClientContext(cfg)
    defer: ctx.close()
    let pool = newHttpPool()
    defer: pool.close()

    let fetched = fetchAndVerifyManifest(ctx, pool, endpoint, entryKeyHex)
    check fetched.payloads.len == 1

    # ---- Cold substitute ----
    let coldStart = epochTime()
    let cold = fetchPayloadStreaming(ctx, pool, endpoint, fetched.payloads[0])
    let coldElapsed = epochTime() - coldStart
    let throughputMiBs = float(cold.bytesIn) / (1024.0 * 1024.0) / coldElapsed
    echo "==== A2.5 P8 cold substitute ===="
    echo "  payload bytes        : ", cold.bytesIn
    echo "  wall-clock seconds   : ", coldElapsed.formatFloat(ffDecimal, 3)
    echo "  throughput MiB/s     : ", throughputMiBs.formatFloat(ffDecimal, 1)
    check cold.bytesIn == int64(BenchPayloadBytes)
    check cold.payloadHash == payloadDigest
    # Loopback budget: 30 s is generous (the actual disk write is the
    # bottleneck on commodity SATA SSD).
    check coldElapsed < 30.0

    # ---- Warm substitute (hash-only re-verification) ----
    let warmStart = epochTime()
    let warm = fetchPayloadStreaming(ctx, pool, endpoint, fetched.payloads[0])
    let warmElapsed = epochTime() - warmStart
    echo "==== A2.5 P8 warm substitute ===="
    echo "  wall-clock seconds   : ", warmElapsed.formatFloat(ffDecimal, 3)
    check warm.bytesIn == int64(BenchPayloadBytes)
    check warm.payloadHash == payloadDigest
    # Warm hit re-reads the local CAS file and BLAKE3s it; on
    # commodity disk this should complete in <= 2 s for 85 MiB.
    check warmElapsed < 5.0

    try: removeDir(clientRoot) except CatchableError: discard
    try: removeDir(serverRoot) except CatchableError: discard
