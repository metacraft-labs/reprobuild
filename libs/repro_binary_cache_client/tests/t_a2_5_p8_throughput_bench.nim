## A2.5 P8 — throughput benchmark (gcc-sized payload, bench-only mode).
##
## Per the campaign spec § Integration test — THROUGHPUT BENCHMARKS:
##
##   t_a2_5_throughput_gcc_15: substitute the gcc-15.2.0 entry (~85 MB
##   compressed payload) and compare wall-clock against Nix on a
##   localhost cache. Pass gate: wall-clock <= 1.0x Nix.
##
## **Bench-only mode (this gate's default).** Building a parallel
## Nix binary-cache + the gcc-15.2.0 NAR archive is non-trivial work
## that requires either a Nix install (Linux/WSL) or NixOnWindows
## (in beta, no first-class support). We report the A2.5 numbers
## here and document a separate Linux-only follow-up gate to run
## ``nix-store --realise`` against the equivalent NAR. The numbers
## generated by this gate are the ones the PERFORMANCE-BASELINE.md
## doc tracks.
##
## ## What we measure
##
##   1. **Cold substitute** of an 85 MiB synthetic payload: time
##      from ``executeSubstituteAction()`` entry to ``ok=true``.
##   2. **Wire throughput**: bytes / wall-clock.
##   3. **Warm fast-path**: a second substitute on the same payload;
##      the index-hit branch should fire and report a sub-100 ms
##      wall-clock.
##
## The throughput gate proper passes when:
##   * Cold substitute completes within 30 s for the 85 MiB
##     synthetic payload on the local loopback.
##   * Warm fast-path completes within 1 s.
##   * The streaming sink's reported ``bytesIn`` equals the
##     manifest's declared payload size.
##
## ## Single-pass-hash assertion
##
## The streaming sink chain hashes bytes WHILE they're being written
## to disk; we never read the temp file back to compute the
## payload's BLAKE3. Asserted indirectly: ``sinkRes.bytesIn`` is the
## sum of HTTP receive callback chunks; ``sinkRes.payloadHash`` is
## the result of the same BLAKE3 instance updated inside the same
## callback. There is no second loop reading the on-disk file.

