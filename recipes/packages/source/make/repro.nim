package makeSource:
  ## From-source make ŌĆö M9.N Batch E compiler-chain slice.
  ##
  ## Tier-2b from-source-autotools convention consumer: the
  ## convention layer reads the ``fetch:`` block (registered via
  ## ``registeredFetchSpec``) and the ``configureFlags:`` block
  ## (registered via ``registeredBuildFlags`` on the ``"configure"``
  ## channel) and lowers them into fetch + configure + build + install
  ## BuildActions. Single executable artifact recipe.

  versions:
    ## Pinned upstream tag. ``sourceUrl`` records the canonical
    ## ftp.gnu.org release tarball URL; ``sourceRepository`` points
    ## at the canonical savannah.gnu.org mirror.
    "4.4.1":
      sourceRevision = "v4.4.1"
      sourceUrl = "https://ftp.gnu.org/gnu/make/make-4.4.1.tar.gz"
      sourceRepository = "https://git.savannah.gnu.org/git/make.git"

  fetch:
    ## Live upstream URL ŌĆö NOT vendored, per the Batch D + E
    ## precedent.
    ##
    ## sha256 computed locally over the upstream ftp.gnu.org tarball;
    ## nixpkgs consumes the same tarball via fetchurl so the cross-
    ## check holds when both pin the same version.
    url: "https://ftp.gnu.org/gnu/make/make-4.4.1.tar.gz"
    sha256: "dd16fb1d67bfab79a72f5e8390735c49e3e8e70b4945a15ab1f81ddb78658fb3"
    extractStrip: 1

  nativeBuildDeps:
    ## gcc is the host C toolchain ŌĆö make is C99.
    "gcc >=11"
    ## perl is invoked by make's ``configure`` script for a handful
    ## of code-generation passes under ``doc/``.
    "perl >=5.32"
    ## M9.R.13b.2 ŌĆö declare ``make`` as a self-dep so the M9.R.10a
    ## cycle-break detects the self-loop and falls through to stdlib
    ## provisioning (nix / scoop / tarball) for the bootstrap make.
    ## Without this, the ``autotools_package`` constructor's
    ## ``make.call(...)`` typed-CLI call emits an action referencing
    ## ``makeBin`` with no profile resolved (``make``'s own toolUses
    ## omit it), and the lowering step hard-fails with
    ## ``action makebin-... references executable makeBin but no tool
    ## profile was resolved for it``. The dep is conceptually correct
    ## anyway: GNU make's upstream ``./configure && make && make
    ## install`` cycle requires a pre-existing ``make`` to drive the
    ## ``make`` + ``make install`` steps; the bootstrap-from-shell
    ## ``./build.sh`` fallback is a future R5 musl-tcc concern.
    "make >=4"

  config:
    ## No prefix lifted from `configureFlags:`; flags inlined in the `build:` block.
    discard
  executable make:
    ## ``$PREFIX/bin/make`` ŌĆö the canonical build-system driver. v1
    ## records the artifact only.
    discard

  build:
    ## M9.R.5b ŌĆö explicit `build:` block constructed from the lifted `config:` values + the inlined verbatim flags. Calls the M9.R.2b high-level `autotools_package(...)` constructor.
    setCurrentOwningPackageOverride("makeSource")
    try:
      let opts = @[
        "--disable-nls",
      ]
      let pkg = autotools_package(srcDir = "./src", configureOptions = opts)
      discard pkg.executable("make")
    finally:
      clearCurrentOwningPackageOverride()

  runtimeDeps:
    ## TODO(M9.R.5b): derive runtime closure from pkg-config /
    ## DT_NEEDED inspection of the linked artifacts. Empty until
    ## the M9.R.5b per-recipe pass populates per-output ELF
    ## interrogation.
    discard
