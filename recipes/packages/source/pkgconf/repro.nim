## Source-from-tarball pkgconf recipe — M9.N Batch D build-tool slice.
##
## pkgconf is the modern, ISC-licensed replacement for the original
## freedesktop ``pkg-config`` C utility: it reads ``.pc`` package
## metadata files under ``/usr/lib/pkgconfig/`` +
## ``/usr/share/pkgconfig/`` and synthesises the compiler + linker
## flags downstream consumers need. Every autotools- /
## meson- / cmake-driven recipe in ``recipes/packages/source/`` that
## probes a system library at configure time shells through pkgconf
## (or its symlinked ``pkg-config`` alias).
##
## ## Convention chosen — ``from-source-autotools``
##
## pkgconf is itself an autotools-driven project: the upstream
## release tarball ships a ``./configure`` script generated by
## autoconf + ``Makefile.in`` files generated by automake. The
## ``from-source-autotools`` convention (M9.L.2) claims the recipe
## via the populated ``configureFlags:`` channel.
##
## ## Why pkgconf matters for the v1 desktop story
##
## pkgconf is the package-metadata-probe utility every modern
## C / C++ build system relies on. Every autotools recipe that
## declares ``PKG_CHECK_MODULES`` shells out to pkgconf. Every meson
## recipe with a ``dependency('foo')`` call shells out to pkgconf.
## Every cmake recipe with ``find_package(PkgConfig REQUIRED)`` +
## ``pkg_check_modules(...)`` shells out to pkgconf.
## Bootstrapping pkgconf from source closes the
## ``uses: "pkg-config"`` toolchain floor every C / C++ recipe pins.
##
## ## sha256 strategy
##
## The fetch URL points at the upstream distfiles.ariadne.space
## release tarball ``pkgconf-2.3.0.tar.xz``. The sha256 was computed
## locally by downloading the tarball and running ``sha256sum`` over
## the bytes. nixpkgs's ``pkgs/by-name/li/libpkgconf/package.nix``
## pins pkgconf 2.5.1 (via the github.com archive URL with SRI
## ``sha256-eXIbrcrRmH3q2cNgnrSHerm1iCHAa9rLgk8siJfBHyo=``), NOT the
## 2.3.0 release the task brief requests, so the nixpkgs cross-check
## is deferred to the follow-up pass that lifts pkgconf to nixpkgs's
## 2.5 line.
##
## ## Version choice — 2.3.0 (per task brief)
##
## pkgconf releases are cut on github.com/pkgconf/pkgconf under
## tags of the form ``pkgconf-<X>.<Y>.<Z>``. 2.3.0 is the version
## the task brief requests; anything ``>=1.8`` covers every
## consumer's ``pkg-config`` pinning (pkgconf is API-compatible with
## the freedesktop pkg-config protocol).
##
## ## Artifacts
##
## pkgconf exposes one load-bearing CLI binary + one library on
## disk:
##
##   * ``pkgconf``    — ``$PREFIX/bin/pkgconf``, the CLI driver every
##                       configure / meson / cmake build system
##                       invokes (often via a ``pkg-config`` symlink
##                       added by the install body — symlink left as
##                       a follow-up).
##   * ``libpkgconf`` — ``$PREFIX/lib/libpkgconf.so``, the pkgconf
##                       internals exposed as a C library so other
##                       tooling (IDEs, build-system generators) can
##                       query ``.pc`` metadata without shelling out
##                       to the CLI.
##
## ## Configurables
##
## v1 ships THREE configure flags mirroring the modern-desktop
## baseline + the system-library-path discovery the brief specifies:
##
##   * ``--disable-static``                       — skip the static
##                                                   archive.
##   * ``--with-system-libdir=/lib:/usr/lib``     — set the default
##                                                   system library
##                                                   search path so
##                                                   pkgconf knows
##                                                   which RPATH-
##                                                   embedded library
##                                                   dirs are
##                                                   default-system.
##   * ``--with-system-includedir=/usr/include``  — set the default
##                                                   system include
##                                                   search path so
##                                                   pkgconf knows
##                                                   which ``-I`` dirs
##                                                   are default-
##                                                   system.

