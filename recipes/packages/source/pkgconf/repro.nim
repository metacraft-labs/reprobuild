package pkgconfSource:
  ## From-source pkgconf — M9.N Batch D build-tool slice.
  ##
  ## Tier-2b from-source-autotools convention consumer: the
  ## convention layer reads the ``fetch:`` block (registered via
  ## ``registeredFetchSpec``) and the ``configureFlags:`` block
  ## (registered via ``registeredBuildFlags`` on the ``"configure"``
  ## channel) and lowers them into fetch + configure + build + install
  ## BuildActions. Mixed-kind one-executable + one-library artifact
  ## recipe.

  versions:
    ## Pinned upstream tag. ``sourceUrl`` records the canonical
    ## distfiles.ariadne.space release tarball URL;
    ## ``sourceRepository`` points at the canonical github.com
    ## project tree.
    "2.3.0":
      sourceRevision = "pkgconf-2.3.0"
      sourceUrl = "https://distfiles.ariadne.space/pkgconf/pkgconf-2.3.0.tar.xz"
      sourceRepository = "https://github.com/pkgconf/pkgconf"

  fetch:
    ## Live upstream URL. The vendor/ directory is reserved for a
    ## follow-up vendoring pass.
    ##
    ## sha256 computed locally over the upstream
    ## distfiles.ariadne.space tarball; nixpkgs cross-check deferred
    ## because nixpkgs pins pkgconf 2.5.1 (not the 2.3.0 the task
    ## brief requests).
    url: "https://distfiles.ariadne.space/pkgconf/pkgconf-2.3.0.tar.xz"
    sha256: "3a9080ac51d03615e7c1910a0a2a8df08424892b5f13b0628a204d3fcce0ea8b"
    extractStrip: 1

  nativeBuildDeps:
    ## autoconf generates the upstream ``configure`` script when the
    ## release tarball ships a stale ``configure.ac``.
    "autoconf"
    ## automake provides the ``Makefile.in`` templates.
    "automake"
    ## libtool drives the shared-library build for libpkgconf.
    "libtool"
    ## make is the build-system driver.
    "make"
    ## gcc is the host C toolchain — pkgconf is C99.
    "gcc >=11"

  config:
    ## No prefix lifted from `configureFlags:`; flags inlined in the `build:` block.
    discard
  executable pkgconf:
    ## ``$PREFIX/bin/pkgconf`` — the CLI driver every configure /
    ## meson / cmake build system invokes. A ``pkg-config`` symlink
    ## is conventionally added by the install body but lives in a
    ## follow-up (the v1 stage-copy step only handles the direct
    ## binary). v1 records the artifact only.
    discard

  library libpkgconf:
    ## ``$PREFIX/lib/libpkgconf.so`` — the pkgconf internals exposed
    ## as a C library so other tooling can query ``.pc`` metadata
    ## without shelling out to the CLI. v1 records the artifact
    ## only.
    discard

  build:
    ## M9.R.5b — explicit `build:` block constructed from the lifted `config:` values + the inlined verbatim flags. Calls the M9.R.2b high-level `autotools_package(...)` constructor.
    setCurrentOwningPackageOverride("pkgconfSource")
    try:
      let opts = @[
        "--disable-static",
        "--with-system-libdir=/lib:/usr/lib",
        "--with-system-includedir=/usr/include",
      ]
      let pkg = autotools_package(srcDir = "./src", configureOptions = opts)
      discard pkg.executable("pkgconf")
      discard pkg.library("libpkgconf")
    finally:
      clearCurrentOwningPackageOverride()

  runtimeDeps:
    ## TODO(M9.R.5b): derive runtime closure from pkg-config /
    ## DT_NEEDED inspection of the linked artifacts. Empty until
    ## the M9.R.5b per-recipe pass populates per-output ELF
    ## interrogation.
    discard
