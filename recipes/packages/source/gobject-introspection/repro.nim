## Source-from-tarball gobject-introspection recipe — M9.R.15b
## GNOME-stack foundation, prereq of gtk4.
##
## gobject-introspection is the build-time toolchain that scans
## GObject-using C libraries (gtk4, glib2, mutter, ...) and emits
## ``.gir`` (XML) + ``.typelib`` (binary) metadata describing every
## exposed type / method / signal / property. Language bindings
## (gjs / pygobject / Vala) consume the .typelib files to autogenerate
## bindings. gtk4 4.22.x uses gobject-introspection at build time
## even when no .gir / .typelib output is requested — the meson
## ``gnome.generate_gir()`` helper is invoked unconditionally for the
## v1 closure, so the gobject-introspection-1.0 pkg-config probe
## must succeed.
##
## ## Why gobject-introspection matters for the v1 desktop story
##
## NDE-G1 (the GNOME-stack DE entry) currently runs in two modes:
##
##   * **v1 closure** — the introspection output is NOT consumed at
##     runtime; gjs / pygobject are deferred to M9.R.15c, gnome-shell
##     is deferred, no Vala consumer exists yet. But gtk4's meson
##     build still requires ``gobject-introspection-1.0.pc`` and
##     ``g-ir-scanner`` on PATH at configure time (set
##     ``introspection=disabled`` on gtk4's options if you want to
##     skip the dep entirely; v1 keeps it on so the cache key is
##     stable across the M9.R.15c gjs+mozjs landing).
##
##   * **future** — gjs (M9.R.15c) consumes the .typelib for every
##     GObject library in the runtime, gnome-shell's JS extension
##     host depends on the full introspection metadata at runtime.
##
## ## sha256 strategy
##
## Per the network + audio batch convention (alsa-lib + kernel
## precedent): live ``fetch:`` URL points at upstream directly (no
## vendoring). gobject-introspection is published on download.gnome.org
## under the ``mirror://gnome/sources/gobject-introspection/`` alias;
## sha256 cross-checked against nixpkgs's
## ``pkgs/development/libraries/gobject-introspection/default.nix``
## SRI hash ``sha256-kg0aP87ercMqz/lcLiA7MZA53UtKCN0aLf0oPRnAua4=``
## (decodes to the hex value pinned below).
##
## ## Version choice — 1.86.0 (current upstream stable)
##
## gobject-introspection follows the GNOME release cadence; 1.86.0
## is the current stable in the 1.86.x line. Matches the nixpkgs pin
## as of mid-2026.
##
## sha256 = 920d1a3fcedeadc32acff95c2e203b319039dd4b4a08dd1a2dfd283d19c0b9ae
##  (cross-checked against nixpkgs's SRI hash above; same upstream
##  download.gnome.org tarball bytes.)
##
## ## Build shape
##
## Meson + ninja. Build-time tools needed: flex, bison, pkg-config,
## python3 with ``setuptools`` + ``mako`` + ``markdown`` modules
## (surfaced via the M9.R.15d.2 ``python3-with-modules`` stdlib stub
## which wraps ``nixpkgs#python3.withPackages``). v1 elides the docs
## build (``gtk_doc=false``) so the docbook-xsl-nons + docbook-xml-dtd
## dependencies are skipped.
##
## ## Library + executable artifacts
##
## gobject-introspection's meson build emits one shared library and
## several command-line tools — v1 records the load-bearing ones:
##
##   * ``libgirepository-1.0.so`` — the runtime metadata loader (every
##                                   downstream language binding links
##                                   against this).
##   * ``g-ir-scanner``           — the build-time scanner that emits
##                                   .gir XML from a C library.
##   * ``g-ir-compiler``          — the .gir -> .typelib compiler.
##
## ## Configurables
##
## v1 ships NO configurables — meson options are pinned:
##
##   * ``cairo=disabled``    — drop Cairo support (cairo lives in a
##                              separate package; introspection
##                              metadata for cairo is generated by
##                              the cairo recipe, not this one).
##   * ``gtk_doc=false``     — drop the HTML docs build.
##   * ``tests=false``       — skip the upstream test suite.
##   * ``doctool=disabled``  — drop the docbook-emit tool (depends on
##                              docbook-xsl, not in the v1 closure).

