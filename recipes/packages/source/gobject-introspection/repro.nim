package gobjectIntrospectionSource:
  ## From-source gobject-introspection — GNOME-stack foundation for
  ## gtk4 (build-time only in v1) and every downstream introspected
  ## library.

  versions:
    "1.86.0":
      sourceRevision = "1.86.0"
      sourceUrl = "https://download.gnome.org/sources/gobject-introspection/1.86/gobject-introspection-1.86.0.tar.xz"
      sourceRepository = "https://gitlab.gnome.org/GNOME/gobject-introspection"

  fetch:
    url: "https://download.gnome.org/sources/gobject-introspection/1.86/gobject-introspection-1.86.0.tar.xz"
    sha256: "920d1a3fcedeadc32acff95c2e203b319039dd4b4a08dd1a2dfd283d19c0b9ae"
    extractStrip: 1

  nativeBuildDeps:
    "meson >=0.62"
    "ninja >=1.10"
    "gcc >=11"
    ## flex generates the GIR lexer at build time.
    "flex"
    ## bison generates the GIR parser at build time.
    "bison"
    ## python3 + the mako/markdown packages run the scanner's
    ## template-driven code generators. M9.R.15d.2 introduced the
    ## ``python3-with-modules`` stdlib stub that wraps python3 with
    ## ``setuptools``, ``mako`` and ``markdown`` pre-loaded — bare
    ## ``python3`` is insufficient because the scanner's startup
    ## ``import mako`` short-fails on a plain interpreter.
    "python3-with-modules"

  buildDeps:
    ## glib2 is the foundation library (libgirepository links against
    ## libgobject + libglib + libgio).
    "glib2 >=2.62"
    ## libffi is required for libgirepository's runtime closure layer
    ## (every introspected callback / signal handler is dispatched
    ## through a libffi-built closure).
    "libffi"

  config:
    discard

  library libGirepository:
    ## ``libgirepository-1.0.so`` — the runtime metadata loader.
    discard

  executable gIrScanner:
    ## ``/usr/bin/g-ir-scanner`` — build-time scanner.
    discard

  executable gIrCompiler:
    ## ``/usr/bin/g-ir-compiler`` — .gir to .typelib compiler.
    discard

  executable gobjectIntrospection:
    ## M9.R.15g.1 — alias artefact so the from-source resolver matches
    ## the dep selector ``"gobject-introspection"`` to this recipe
    ## without the canonical-prefix tier needing to reason about the
    ## ``g-ir-*`` binary naming convention. The build block stages
    ## ``g-ir-scanner`` at ``.repro/output/gobject-introspection/
    ## gobject-introspection`` via ``pkg.executableAlias(...)``; the
    ## actual entry-point is still ``g-ir-scanner`` (consumers walk
    ## the install-mirror's ``usr/bin/`` directly when invoking the
    ## scanner — the alias only serves the resolver's name-match).
    name: "gobject-introspection"
    discard

  build:
    setCurrentOwningPackageOverride("gobjectIntrospectionSource")
    try:
      let opts = @[
        "cairo=disabled",
        "gtk_doc=false",
        "tests=false",
        "doctool=disabled",
        # M9.R.16.3 — pin ``libdir=lib`` (overrides meson's default of
        # ``lib64``). The upstream ``g-ir-scanner`` script (installed
        # to ``$prefix/bin``) computes its ``pylibdir`` relocatably as
        # ``$prefix/../lib/gobject-introspection``. When meson uses the
        # default ``lib64``, the ``giscanner`` Python module lands at
        # ``$prefix/lib64/gobject-introspection/giscanner/`` and the
        # scanner's relocatable probe misses it, falling through to a
        # hard-coded ``/usr/lib64/gobject-introspection`` (also missing
        # in the install-mirror), and the scanner aborts with
        # ``ModuleNotFoundError: No module named 'giscanner'`` during
        # any downstream ``g-ir-scanner`` call (e.g. gdm's
        # ``libgdm/Gdm-1.0.gir`` step). Pinning ``libdir=lib`` keeps
        # the relocatable probe happy. The install-mirror's pkg-config
        # rewrite path already handles both ``lib`` and ``lib64``
        # (``package_result.nim:1020-1021``).
        "libdir=lib",
      ]
      let pkg = meson_package(srcDir = "./src", configureOptions = opts)
      discard pkg.library("libGirepository")
      discard pkg.executable("gIrScanner")
      discard pkg.executable("gIrCompiler")
      # M9.R.15g.1 — alias ``gobject-introspection`` to ``g-ir-scanner``
      # so consumers writing ``nativeBuildDeps: "gobject-introspection"``
      # match this recipe at the resolver's tier-0 exact-match level.
      discard pkg.executableAlias("gobject-introspection",
        sourceName = "g-ir-scanner")
    finally:
      clearCurrentOwningPackageOverride()

  runtimeDeps:
    discard
