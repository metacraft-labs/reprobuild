## Source-from-tarball binutils recipe — M9.N Batch E compiler-chain
## slice.
##
## binutils is the GNU binary utilities collection: the canonical host
## linker / assembler / archiver / object-file inspector toolchain
## every from-source recipe under ``recipes/packages/source/`` that
## drives gcc transitively consumes at link + assemble + strip time.
## The Batch E (gcc + binutils + make) slice sits BELOW Batch D (cmake
## + autoconf + automake + libtool + pkgconf) in the bootstrap
## layering: every Batch D autotools recipe ultimately shells out to
## ``ld`` / ``as`` / ``ar`` from binutils. R5 musl-tcc is the long-
## term clean root for the bootstrap chain; Batch E ships the recipes
## against the existing upstream tarballs as declarative shape so the
## M9.K convention layer can lower them when the recipe graph asks for
## the host binary-utilities toolchain.
##
## ## Convention chosen — ``from-source-autotools``
##
## binutils is itself an autotools-driven project: the upstream
## release tarball ships a ``./configure`` script generated by
## autoconf + ``Makefile.in`` files generated by automake at the
## source root, so the canonical ``./configure && make && make
## install`` cycle suffices. The ``from-source-autotools`` convention
## (M9.L.2) claims the recipe via the populated ``configureFlags:``
## channel.
##
## binutils is the ONLY ``from-source-autotools`` consumer in the
## corpus that declares FIVE configure flags. The prior precedents
## (expat = 4, pkgconf = 3, autoconf / automake / libtool = 1) cover
## the 1 / 3 / 4-flag cardinalities; binutils closes the 5-flag gap
## with the canonical ``--enable-gold`` + ``--enable-ld=default`` +
## ``--enable-plugins`` + ``--enable-shared`` + ``--disable-werror``
## set the modern desktop story requires.
##
## ## Why binutils matters for the v1 desktop story
##
## binutils is the binary-utilities toolchain every C / C++ recipe in
## ``recipes/packages/source/`` that produces a linkable artifact
## (executables / shared libraries / static archives) depends on:
## every gcc invocation shells out to ``ld`` at link time, every
## archive build invokes ``ar`` + ``ranlib``, every strip pass runs
## ``strip``, every readelf-based probe shells through ``readelf``.
## Bootstrapping binutils from source closes the host binary-
## utilities floor the entire C / C++ ecosystem pins.
##
## ## sha256 strategy
##
## The fetch URL points at the upstream ftp.gnu.org release tarball
## ``binutils-2.43.tar.xz``. The sha256 was computed locally by
## downloading the tarball and running ``sha256sum`` over the bytes.
## nixpkgs's ``pkgs/development/tools/misc/binutils/default.nix``
## consumes the same ftp.gnu.org tarball via fetchurl so the cross-
## check holds when both pin the same version.
##
## ## Version choice — 2.43 (per task brief)
##
## binutils releases are cut on ftp.gnu.org under tags of the form
## ``binutils-<X>.<Y>``. 2.43 is the latest stable in the 2.43.x line
## as of the task brief. The 2.43 cut introduced the
## ``--enable-default-execstack=no`` defence-in-depth default + the
## RISC-V ZBA / ZBB / ZBC extension support; anything ``>=2.39``
## covers the kernel-recipe pinning and every consumer's binutils
## floor.
##
## ## Why binutils is NOT vendored
##
## The ``binutils-2.43.tar.xz`` tarball weighs ~27 MB which is well
## below GitHub's 100-MB single-file ceiling, but the Batch D + E
## precedents (cmake / autoconf / automake / libtool / pkgconf / gcc)
## all use live upstream URLs directly — no vendor/ copy. binutils
## follows the same precedent. A future R5 musl-tcc vendoring pass
## will host the bootstrap-critical archives on a reprobuild-managed
## mirror.
##
## ## Artifacts
##
## binutils exposes ELEVEN load-bearing CLI binaries on disk:
##
##   * ``ld``        — ``$PREFIX/bin/ld``, the canonical linker every
##                      gcc / g++ invocation shells out to at link
##                      time. The ``--enable-ld=default`` configure
##                      flag pins the ``ld`` binary to be the
##                      classical BFD linker (vs gold / lld).
##   * ``as``        — ``$PREFIX/bin/as``, the GNU assembler every
##                      gcc invocation shells out to for the
##                      ``.s`` -> ``.o`` pass.
##   * ``ar``        — ``$PREFIX/bin/ar``, the archive builder every
##                      static-library build invokes to bundle
##                      ``.o`` files into a ``.a`` archive.
##   * ``nm``        — ``$PREFIX/bin/nm``, the symbol-table dumper
##                      every linker-debug session reaches for.
##   * ``objcopy``   — ``$PREFIX/bin/objcopy``, the object-file
##                      surgeon (rename / strip / extract sections /
##                      convert formats) the kernel build invokes
##                      during the ``arch/x86/boot/`` pass.
##   * ``objdump``   — ``$PREFIX/bin/objdump``, the disassembler +
##                      object-file inspector every reverse-
##                      engineering / debugging workflow consumes.
##   * ``ranlib``    — ``$PREFIX/bin/ranlib``, the archive-index
##                      generator paired with ``ar`` for legacy
##                      archives that need an explicit symbol table.
##   * ``strip``     — ``$PREFIX/bin/strip``, the symbol-stripper
##                      every release-build pass invokes to remove
##                      debug + symbol-table sections from shipped
##                      binaries.
##   * ``readelf``   — ``$PREFIX/bin/readelf``, the ELF-format
##                      inspector every loader-debug session +
##                      every autotools ``./configure`` probe for
##                      DT_NEEDED / RPATH / SONAME shells through.
##   * ``size``      — ``$PREFIX/bin/size``, the section-size
##                      reporter that summarises text / data / bss
##                      footprint for a binary.
##   * ``strings``   — ``$PREFIX/bin/strings``, the printable-ASCII-
##                      extractor that surfaces string literals from
##                      a binary (forensics / reverse-engineering
##                      staple).
##
## ## Configurables
##
## v1 ships FIVE configure flags per the task brief:
##
##   * ``--enable-gold``      — enable the gold linker as a sidecar
##                               binary. gold is a faster, more-
##                               parallel linker for ELF targets;
##                               retaining it gives consumer
##                               recipes a choice between BFD ld and
##                               gold.
##   * ``--enable-ld=default`` — pin the ``$PREFIX/bin/ld`` binary
##                                to be the classical BFD linker (vs
##                                making gold the default). The
##                                classical BFD ld is the more
##                                conservative default that every
##                                consumer recipe expects to find.
##   * ``--enable-plugins``    — build with linker-plugin support so
##                                gcc's LTO pass can hand object
##                                files to a plugin-loaded helper
##                                at link time.
##   * ``--enable-shared``     — emit the binutils libraries as
##                                shared objects (libbfd.so /
##                                libopcodes.so / libctf.so) in
##                                addition to the static archives.
##                                The desktop-baseline recipes that
##                                link against libbfd at runtime
##                                (the ``binutils-devel`` consumer
##                                family) need the .so variants.
##   * ``--disable-werror``    — treat compiler warnings as warnings,
##                                not errors. The upstream binutils
##                                tree occasionally trips ``-Werror``
##                                on newer gcc / clang versions; the
##                                task brief specifies this defence-
##                                in-depth flag to keep the build
##                                resilient to host-compiler version
##                                drift.

