    "2.43":
      sourceRevision = "binutils-2_43"
      sourceUrl = "https://ftp.gnu.org/gnu/binutils/binutils-2.43.tar.xz"
      sourceRepository = "https://sourceware.org/git/binutils-gdb.git"

