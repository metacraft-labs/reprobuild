  build:
    setCurrentOwningPackageOverride("btrfsProgsSource")
    try:
      let opts = @[
        "--disable-static",
        "--enable-shared",
        "--disable-documentation",
        "--disable-python",
        "--disable-zoned",
        "--disable-libudev",
        "--disable-convert",
      ]
      ## M9.R.29.12c — btrfs-progs uses an in-tree Makefile (the
      ## upstream Makefile lives in src/Makefile, generated by
      ## configure from src/Makefile.in). The standard
      ## ``autotools_package`` out-of-tree pattern leaves ``build/``
      ## without a Makefile; pin ``buildDir = "src"`` so configure
      ## emits the Makefile next to the sources and make finds it.
      let pkg = autotools_package(srcDir = "./src", buildDir = "src",
                                  configureOptions = opts)
      discard pkg.executable("btrfs")
      discard pkg.executableAlias("mkfsBtrfs", "mkfs.btrfs")
      discard pkg.executable("btrfsck")
      discard pkg.executableAlias("btrfsImage", "btrfs-image")
      discard pkg.executableAlias("btrfsTune", "btrfstune")
      discard pkg.library("libBtrfs")
    finally:
      clearCurrentOwningPackageOverride()

  runtimeDeps:
    discard
