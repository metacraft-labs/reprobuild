## Source-from-tarball libtool recipe ‚Äî M9.N Batch D build-tool slice.
##
## libtool is the GNU build system's library-compilation abstraction
## layer: it hides the per-platform shared-library quirks (RPATH /
## SONAME / DT_NEEDED / -fPIC / .la archive metadata) behind a
## uniform ``./libtool --mode=compile`` / ``./libtool --mode=link``
## CLI. Autotools projects that build shared libraries
## (``--disable-static`` and friends) drive libtool from their
## generated ``Makefile``s for every compilation + link step.
##
## ## Convention chosen ‚Äî ``from-source-autotools``
##
## libtool is itself an autotools-driven project: the upstream
## release tarball ships a ``./configure`` script generated by
## autoconf + ``Makefile.in`` files generated by automake. The
## ``from-source-autotools`` convention (M9.L.2) claims the recipe
## via the populated ``configureFlags:`` channel.
##
## ## Why libtool matters for the v1 desktop story
##
## libtool is the shared-library abstraction every autotools-driven
## C / C++ recipe in ``recipes/packages/source/`` that produces a
## shared object depends on: expat, libcap, libcap-ng, gettext, xz,
## ncurses, readline, libffi, libgcrypt, libxml2, nettle, gnutls,
## libtasn1, libidn2, gawk, ... all drive ``./libtool`` from their
## ``Makefile``s. Bootstrapping libtool from source closes the
## ``uses: "libtool"`` toolchain floor those recipes pin.
##
## ## sha256 strategy
##
## The fetch URL points at the upstream ftp.gnu.org release tarball
## ``libtool-2.5.4.tar.xz``. The sha256 was computed locally by
## downloading the ``.tar.xz`` variant and running ``sha256sum`` over
## the bytes. nixpkgs's
## ``pkgs/development/tools/misc/libtool/libtool2.nix`` pins libtool
## 2.5.4 but consumes the ``.tar.gz`` variant with SRI
## ``sha256-2o67LOTc9GuQCY2vliz/po9LT2LqYPeY0O8Skp7eat8=`` ‚Äî NOT the
## ``.tar.xz`` variant the task brief requests, so the nixpkgs cross-
## check is byte-incompatible (different compressor).
##
## ## Version choice ‚Äî 2.5.4 (per task brief; matches nixpkgs)
##
## libtool releases are cut on ftp.gnu.org under tags of the form
## ``libtool-<X>.<Y>.<Z>``. 2.5.4 is the current stable in the 2.5.x
## line and the same version nixpkgs pins; anything ``>=2.4`` covers
## every consumer's libtool pinning.
##
## ## Artifacts
##
## libtool exposes two load-bearing CLI binaries + one library on
## disk:
##
##   * ``libtool``    ‚Äî ``$PREFIX/bin/libtool``, the canonical shell
##                       driver that wraps compile / link / install /
##                       finish / execute operations behind a uniform
##                       CLI.
##   * ``libtoolize`` ‚Äî ``$PREFIX/bin/libtoolize``, the per-project
##                       libtool installer driven by ``autoreconf``.
##   * ``libltdl``    ‚Äî ``$PREFIX/lib/libltdl.so``, the dynamic-module
##                       loader library autotools projects link
##                       against when they need to ``dlopen``-style
##                       load plugins portably.
##
## ## Configurables
##
## v1 ships ONE configure flag ‚Äî ``--disable-static`` ‚Äî mirroring the
## modern-desktop baseline. libtool's own libltdl honours the flag
## and emits only the shared variant.

