package libtoolSource:
  ## From-source libtool ‚Äî M9.N Batch D build-tool slice.
  ##
  ## Tier-2b from-source-autotools convention consumer: the
  ## convention layer reads the ``fetch:`` block (registered via
  ## ``registeredFetchSpec``) and the ``configureFlags:`` block
  ## (registered via ``registeredBuildFlags`` on the ``"configure"``
  ## channel) and lowers them into fetch + configure + build + install
  ## BuildActions. Mixed-kind two-executable + one-library artifact
  ## recipe.

  versions:
    ## Pinned upstream tag. ``sourceUrl`` records the canonical
    ## ftp.gnu.org release tarball URL; ``sourceRepository`` points
    ## at the canonical savannah.gnu.org mirror.
    "2.5.4":
      sourceRevision = "v2.5.4"
      sourceUrl = "https://ftp.gnu.org/gnu/libtool/libtool-2.5.4.tar.xz"
      sourceRepository = "https://git.savannah.gnu.org/git/libtool.git"

  fetch:
    ## Live upstream URL. The vendor/ directory is reserved for a
    ## follow-up vendoring pass.
    ##
    ## sha256 computed locally over the upstream ftp.gnu.org
    ## ``.tar.xz`` tarball; nixpkgs cross-check is byte-incompatible
    ## because nixpkgs consumes the ``.tar.gz`` variant.
    url: "https://ftp.gnu.org/gnu/libtool/libtool-2.5.4.tar.xz"
    sha256: "f81f5860666b0bc7d84baddefa60d1cb9fa6fceb2398cc3baca6afaa60266675"
    extractStrip: 1

  nativeBuildDeps:
    ## m4 is the macro processor libtool's bootstrap chain shells out
    ## to for the ``libtool.m4`` macro expansion.
    "m4"
    ## make is the build-system driver ‚Äî the from-source-autotools
    ## convention's compile action invokes ``make`` after
    ## ``./configure``.
    "make"
    ## gcc is the host C toolchain ‚Äî libltdl is C99.
    "gcc >=11"

  buildDeps:
    ## file is consumed by libtool 2.4.7+ for the ``FILECMD`` shim
    ## that replaces the hardcoded ``/usr/bin/file`` reference.
    "file"

  config:
    ## No prefix lifted from `configureFlags:`; flags inlined in the `build:` block.
    discard
  executable libtool:
    ## ``$PREFIX/bin/libtool`` ‚Äî the canonical shell driver wrapping
    ## compile / link / install / finish / execute operations. v1
    ## records the artifact only.
    discard

  executable libtoolize:
    ## ``$PREFIX/bin/libtoolize`` ‚Äî the per-project libtool installer
    ## driven by ``autoreconf``. v1 records the artifact only.
    discard

  library libltdl:
    ## ``$PREFIX/lib/libltdl.so`` ‚Äî the dynamic-module loader library
    ## autotools projects link against when they need portable
    ## ``dlopen`` semantics. v1 records the artifact only.
    discard

  build:
    ## M9.R.5b ‚Äî explicit `build:` block constructed from the lifted `config:` values + the inlined verbatim flags. Calls the M9.R.2b high-level `autotools_package(...)` constructor.
    setCurrentOwningPackageOverride("libtoolSource")
    try:
      let opts = @[
        "--disable-static",
      ]
      let pkg = autotools_package(srcDir = "./src", configureOptions = opts)
      discard pkg.executable("libtool")
      discard pkg.executable("libtoolize")
      discard pkg.library("libltdl")
    finally:
      clearCurrentOwningPackageOverride()

  runtimeDeps:
    ## TODO(M9.R.5b): derive runtime closure from pkg-config /
    ## DT_NEEDED inspection of the linked artifacts. Empty until
    ## the M9.R.5b per-recipe pass populates per-output ELF
    ## interrogation.
    discard
